package com.example.module_ads.enums

/**
 * Enum class representing the position of a collapsible banner on the screen.
 * The banner can be positioned at the top or bottom of the screen.
 */
enum class CollapsibleBannerPosition {
    /**
     * Represents the top position of the collapsible banner.
     * The banner will be displayed at the top of the screen.
     */
    TOP,

    /**
     * Represents the bottom position of the collapsible banner.
     * The banner will be displayed at the bottom of the screen.
     */
    BOTTOM,
    /**
     * Represents the none position of the banner.
     * The normal banner will  be displayed.
     */
    NONE
}
