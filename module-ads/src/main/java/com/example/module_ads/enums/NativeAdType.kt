package com.example.module_ads.enums

/**
 * Enum class representing different types of native ads.
 */
enum class NativeAdType {
    /**
     * Small size native ad type.
     */
    SMALL,

    /**
     * Medium size native ad type.
     */
    MEDIUM,

    /**
     * Large size native ad type.
     */
    LARGE,

    /**
     * Native banner ad type.
     */
    NATIVE_BANNER
}
